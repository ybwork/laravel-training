     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $category = $request['category'];
        $direction = $request['direction'];

        $search_main = DB::table('places');

        if ($category != '') {
            $search_main->where('category_id', '=', $category);
        } 

        if ($direction != '') {
            $search_main->where('direction_id', '=', $direction);
        }
            
        $search = $search_main->get();
        
        return view('places.index', compact('search'));
    }

